class Portfolio extends React.Component {
  constructor() {
    super();
    this.state = {
      selected: 'All',
      filteredProjects: projects
     };
   }

  render() {

    const renderProjects = (projects) => {
        return projects.map((project, i) => {
          return (
            <div className="project" key={`project-${project.category}-${i}`}>
              <img src={project.img} alt={`project-${project.category}-${i}`} />
            </div>
          );
        });
      };

    return (
      <div>
        <Toolbar
          filters={filters}
          selected={this.state.selected}
          onSelectFilter={(filter) => {
            this.setState({ selected: filter });
            if (filter !== 'All'){
                let filterProjects = projects.filter(function(item) {return item.category === filter});
                this.setState({ filteredProjects: filterProjects })
              }
            else { 
              this.setState({filteredProjects: projects })
            }
            console.log(filter)
          }} />
       <div className="portfolio">
          {renderProjects(this.state.filteredProjects)}
      </div>
      </div>
    );

  }

}

  export default Portfolio;